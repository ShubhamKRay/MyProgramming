package array;

public class BasicPrgm1 {
//	WAJP to swap two arrays.
	
		public static void main(String[] args){
			int[] nums={10, 200, 30, 40, 50};
			for(int i=0;i<nums.length;i++)
				System.out.println(nums[i]);
			
			System.out.println("==============");
		
			for(int x:nums)
				System.out.println(x);
						
		
	}
}
